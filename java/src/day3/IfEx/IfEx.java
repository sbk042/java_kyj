package day3.IfEx;

public class IfEx {

	public static void main(String[] args) {
		// 정수 num가 0이면 0입니다 라고 출력하는 코드를 작성하세요.
		// 정수 num가 0이다
		// 정수 num가 0과 같다.
		int num = 10; // num이 얼마인가에 따라 결과가 달라진다.
		if(num == 0) {
			// 0입니다 라고 출력;
			System.out.println("0입니다.");
		}
        // 정수 num가 0이 아니면 num is not 0라고 출력하는 코드를 작성하세요.
		// num가 0과 같지 않다.
		if(num != 0 ) {
			System.out.println(num + " is not 0 ");
		}
	}
 
}
